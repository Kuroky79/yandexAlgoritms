function calculateProfit(n, k, d) {
    // Вычисляем остаток от деления изначальной прибыли на k
    const remainder = n % k;

    // Если остаток равен 0, то на каждый день прибыль уже делится на k,
    // поэтому просто возвращаем исходную прибыль
    if (remainder === 0) {
        return n;
    }

    // Иначе, находим минимальное число, которое больше исходной прибыли и делится на k
    const target = n + (k - remainder);

    // Проверяем, если разница между исходной прибылью и целевой делится на d,
    // то возвращаем целевую прибыль, иначе возвращаем -1
    if ((target - n) % d === 0) {
        return target;
    } else {
        return -1;
    }
}

// Примеры ввода данных
const input1 = [21, 108, 1];
const input2 = [5, 12, 4];

// Вывод результатов
console.log(calculateProfit(...input1)); // Ожидаемый вывод: 216
console.log(calculateProfit(...input2)); // Ожидаемый вывод: -1
